# შექმენით პროგრამა, რომელიც მომხმარებელს რიცხვებს შემოატანინებს მანამ, სანამ უარყოფით რიცხვს არ შეიყვანს:
#   - დაითვალეთ შეყვანილი რიცხვების ჯამი
