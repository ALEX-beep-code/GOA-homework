#   - იყოფა 5-ზე
#   - მაგრამ არ იყოფა 3-ზე
#   (გააკეთეთ ორ ვარიანტში):
#   - for ციკლით
#   - while ციკლით

for i in range (100 , 400):
    if i % 5 == 0:
        if i % 3 != 0:
            print(i)

i = 100
while i < 400:
    if i % 5 == 0 and i % 3 != 0:
       print(i)
    i += 1

# 1 დან 100 მდე დაპრინტეთ ყველა რიცხვი:
#   - თუ რიცხვი იყოფა 2-ზე და 5-ზე --> `EvenFive`
#   - თუ მხოლოდ 2-ზე იყოფა --> `Even`
#   - თუ მხოლოდ 5-ზე იყოფა --> `Five`
#   - სხვა შემთხვევაში --> თავად რიცხვი

i = 1
while i < 100:
    if i % 2 == 0 and i % 5 == 0:
        print("evenfive")
        i += 1
    elif i % 2 == 0:
        print ("even")
        i += 1
    elif i % 5 == 0:
        print ("five")
        i += 1
    else:
        print(i)
        i += 1



